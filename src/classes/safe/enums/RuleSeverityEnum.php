<?php

declare(strict_types=1);

namespace app\modules\neuron\classes\safe\enums;

/**
 * Уровень уверенности/критичности safe-правила.
 *
 * Используется не для принятия решения само по себе, а как документированная
 * характеристика правила: high-правила можно включать по умолчанию, low-правила
 * требуют осторожности из-за риска false-positive.
 *
 * Пример:
 * ```php
 * $severity = RuleSeverityEnum::HIGH;
 * ```
 */
enum RuleSeverityEnum: string
{
    /** Низкая уверенность: правило лучше использовать только в составе более сложной политики. */
    case LOW = 'low';

    /** Средняя уверенность: правило полезно, но требует тестов на false-positive. */
    case MEDIUM = 'medium';

    /** Высокая уверенность: правило подходит для блокировки/редактирования по умолчанию. */
    case HIGH = 'high';
}
