<?php

declare(strict_types=1);

namespace app\modules\neuron\enums;

/**
 * Перечисление типов вложений, поддерживаемых при отправке запросов в LLM.
 *
 * Значение каждого элемента используется как строковый код типа
 * в полезной нагрузке, формируемой DTO вложений.
 */
enum AttachmentTypeEnum: string
{
    /**
     * Дополнительный текстовый блок (контент передаётся напрямую в запрос).
     */
    case TEXT = 'text';

    /**
     * Изображение, передаваемое как данные (URL, base64 и т.п.).
     */
    case IMAGE = 'image';

    /**
     * Текстовый файл, передаваемый по пути или идентификатору ресурса.
     */
    case TEXT_FILE = 'text_file';

    /**
     * Файл изображения, передаваемый по пути или идентификатору ресурса.
     */
    case IMAGE_FILE = 'image_file';
}

