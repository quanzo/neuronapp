<?php

declare(strict_types=1);

namespace app\modules\neuron\interfaces;

/**
 * Интерфейс текстового навыка (Skill).
 *
 * Навык представлен текстовым шаблоном, который может содержать
 * параметры-подстановки вида $1, $2 и т.д., а также набор опций.
 */
interface ISkill
{
    /**
     * Возвращает текст навыка с подставленными параметрами.
     *
     * Каждый параметр подставляется в соответствующий плейсхолдер:
     *  - первый параметр заменяет все вхождения "$1";
     *  - второй параметр заменяет все вхождения "$2" и т.д.
     * Плейсхолдеры без переданных значений остаются без изменений.
     *
     * @param mixed ...$params Значения параметров для подстановки.
     */
    public function getSkill(...$params): string;

    /**
     * Возвращает массив опций, связанных с навыком.
     *
     * @return array<string, mixed>
     */
    public function getOptions(): array;
}

