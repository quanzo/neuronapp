  * Статус: коммит сделан, ждём ревью.

# ОБЯЗАТЕЛЬНО УКАЗАТЬ:
- Главная задача / проблема.
- Конкретные решения и договорённости (что именно сделали или решили сделать).
- Важные идентификаторы, параметры, названия конфигов, версии (если упоминались).
- Текущий статус: что уже выполнено, что осталось.

# __ЗАПРЕЩЕНО:__
- Не выдумывай детали, которых не было в диалоге.
- Не пересказывай длинные объяснения — только итоговый факт.
